package org.ufba.dcc.mata60.model;

public class TurmaLecionadaProfessor {

	private String professor_cpf;
	private String turma_numero;
	private String turma_disciplina_cod;
	private String turma_semestre;
	
	
	public String getProfessor_cpf() {
		return professor_cpf;
	}
	public void setProfessor_cpf(String professor_cpf) {
		this.professor_cpf = professor_cpf;
	}
	public String getTurma_numero() {
		return turma_numero;
	}
	public void setTurma_numero(String turma_numero) {
		this.turma_numero = turma_numero;
	}
	public String getTurma_disciplina_cod() {
		return turma_disciplina_cod;
	}
	public void setTurma_disciplina_cod(String turma_disciplina_cod) {
		this.turma_disciplina_cod = turma_disciplina_cod;
	}
	public String getTurma_semestre() {
		return turma_semestre;
	}
	public void setTurma_semestre(String turma_semestre) {
		this.turma_semestre = turma_semestre;
	}
	
	
	
}
