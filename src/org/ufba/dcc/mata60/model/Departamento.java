package org.ufba.dcc.mata60.model;


public class Departamento {

	private int cod;
	private String nome;
	
	public Departamento(){
		
	}
	
	
	public int getCod() {
		return cod;
	}
	public void setCod(int cod) {
		this.cod = cod;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

	
	
}
